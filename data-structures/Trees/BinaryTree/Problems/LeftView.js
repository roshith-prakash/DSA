// The task is to print the left view of binary tree. Left view of a binary tree is the set of nodes visible when the tree is viewed from the left.
// Any node with same level is hidden by node from left.
// Use same logic as level order traversal.

// Node to represent a singular element in the tree
class Node {
    constructor(value, left, right) {
        this.value = value
        this.left = left
        this.right = right
    }
}

// Function to get leftmost nodes on each level
const leftView = (head) => {
    // Create a Queue
    const queue = []

    // If head is null, return null
    if (head == null) {
        return null
    }

    // Array to push leftmost values for each node
    let arr = []

    // Push root node into queue
    queue.push(head)
    // Push null whenever a level is completed (since root only has one element; add null)
    queue.push(null)

    // Push head value to array as no other nodes are present on same level
    arr.push(head.value)

    // While queue is not empty
    while (queue.length) {

        // Get the node at the front of queue
        let temp = queue[0]
        // Dequeue node
        queue.shift()

        // If value at front was null, level was completed - First element after null is required element
        if (temp == null) {

            // Push required element to array 
            if (queue[0]) {
                arr.push(queue[0]?.value)
            }

            // If queue still has elements, push null
            if (queue.length) {
                queue.push(null)
            }
        }
        // If value at front is a node
        else {
            // Add left node to queue
            if (temp?.left) {
                queue.push(temp?.left)
            }

            // Add right node to queue
            if (temp?.right) {
                queue.push(temp?.right)
            }
        }
    }

    console.log(arr.join(" "))
}

// Class for Binary Tree
class BinaryTree {
    constructor() {
        this.head = null
    }

    // Initialize node as head
    inititializeHead(node) {
        this.head = node
    }

    // Create a node
    createNode(value) {
        const node = new Node(value, null, null)
        return node
    }

    // Initialize node as head
    inititializeHead(node) {
        this.head = node
    }

    // Add node2 to the left of node 1
    addToLeft(node1, node2) {
        if (node1 && node2) {
            node1.left = node2
        }
    }

    // Add node2 to the right of node 1
    addToRight(node1, node2) {
        if (node1 && node2) {
            node1.right = node2
        }
    }

    // Left View of Tree
    leftView() {
        leftView(this.head)
    }

}

